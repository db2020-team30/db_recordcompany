
CREATE TABLE MANAGER (
	manager_id integer PRIMARY KEY AUTO_INCREMENT,
	fullname varchar(50) NOT NULL
);

CREATE TABLE SYMBEBLHMENOS (
	afm varchar(9) PRIMARY KEY,
	firstname varchar(20) NOT NULL,
	lastname varchar(20) NOT NULL,
	address varchar(30),
	city varchar(30),
	TK varchar(5),
	sex varchar(1) CHECK(sex IN ('F','M')),
	doy varchar(30),
	onoma_patros varchar(20),
	Manager_id integer DEFAULT NULL, 
	FOREIGN KEY (Manager_id)REFERENCES MANAGER(manager_id) ON  DELETE SET NULL
);
CREATE TABLE SYMBOLAIO (
	id_contract varchar(15),
	afm varchar(9),
	startdate date NOT NULL,
	enddate date NOT NULL,
	pososto integer NOT NULL CHECK(pososto<=100),
	PRIMARY KEY(id_contract, afm),
	FOREIGN KEY (afm) REFERENCES SYMBEBLHMENOS(afm) ON DELETE CASCADE
);

CREATE TABLE EPIK_SYMBEBL (
	afm varchar(9) PRIMARY KEY ,
	kin1 varchar(10) NOT NULL,
	kin2 varchar(10),
	stathero varchar(10),
	email varchar(40),
	socialmedia varchar(40),
	FOREIGN KEY (afm) REFERENCES SYMBEBLHMENOS(afm) ON DELETE CASCADE
);

CREATE TABLE STIXOURGOS (
	afm varchar(9) PRIMARY KEY,
	FOREIGN KEY (afm) REFERENCES SYMBEBLHMENOS(afm) ON DELETE CASCADE
);

CREATE TABLE SYNTHETHS (
	afm varchar(9) PRIMARY KEY,
	FOREIGN KEY (afm) REFERENCES SYMBEBLHMENOS(afm) ON DELETE CASCADE
);

CREATE TABLE PERSONA_GROUP (
	artist_id integer PRIMARY KEY AUTO_INCREMENT,
	stage_name varchar(50) NOT NULL
);


CREATE TABLE MOUSIKOS (
	afm varchar(9) PRIMARY KEY ,
	specialty varchar(10) NOT NULL,
	artist_id integer DEFAULT NULL,
	FOREIGN KEY (artist_id) REFERENCES PERSONA_GROUP(artist_id) ON DELETE SET NULL,
	FOREIGN KEY (afm) REFERENCES SYMBEBLHMENOS(afm) ON DELETE CASCADE
);


CREATE TABLE EPIK_MANAGER (
	manager_id integer PRIMARY KEY,
	kin1 varchar(10) NOT NULL,
	kin2 varchar(10),
	stathero varchar(10),
	email varchar(40),
	socialmedia varchar(40), 
	FOREIGN KEY (manager_id) REFERENCES MANAGER(manager_id) ON DELETE CASCADE
);

CREATE TABLE GRAFISTAS (
	afm varchar(9) PRIMARY KEY,
	fullname varchar(50) NOT NULL,
	AMKA varchar(11) UNIQUE,
	address varchar(30),
	city varchar(30),
	TK varchar(5)
);


CREATE TABLE STUDIO (
	afm varchar(9) PRIMARY KEY,
	hourly_rate integer NOT NULL,
	address varchar(30) NOT NULL,
	city varchar(30) NOT NULL,
	TK varchar(5),
	onoma varchar(50)
);


CREATE TABLE ALBUM (
	album_id integer PRIMARY KEY AUTO_INCREMENT,
	titlos varchar(50) NOT NULL,
	vinyl_stock integer DEFAULT 0 NOT NULL,
	CD_stock integer DEFAULT 0 NOT NULL,
	release_date date,
	graf_afm varchar(9)  DEFAULT NULL,
	royalties_prof integer,
	studio_afm varchar(9) DEFAULT NULL,
	rec_start date,
	rec_end date,
	hours INTEGER,
	FOREIGN KEY (graf_afm) REFERENCES GRAFISTAS(afm) ON DELETE SET NULL,
	FOREIGN KEY (studio_afm) REFERENCES studio(afm) ON DELETE SET NULL
);

CREATE TABLE TRAGOUDI (
	song_id integer PRIMARY KEY AUTO_INCREMENT,
	diarkeia time NOT NULL,
	titlos varchar(40) NOT NULL,
	s_language varchar(20)
);

CREATE TABLE EIDOS (
	genre varchar(30) PRIMARY KEY
);

CREATE TABLE ANHKEI (
	album_id integer,
	song_id integer ,
	track_no integer,
	PRIMARY KEY(album_id,song_id, track_no),
	FOREIGN KEY (album_id)  REFERENCES ALBUM(album_id) ON DELETE CASCADE,
	FOREIGN KEY (song_id) REFERENCES TRAGOUDI(song_id) ON DELETE CASCADE
	
); 

CREATE TABLE GRAFEI (
	afm varchar(9)  ,
	song_id integer ,
	PRIMARY KEY(afm,song_id),
	FOREIGN KEY (afm)REFERENCES STIXOURGOS(afm) ON DELETE CASCADE,
	FOREIGN KEY (song_id) REFERENCES TRAGOUDI(song_id) ON DELETE CASCADE
);

CREATE TABLE SYNTHETEI (
	afm varchar(9)  ,
	song_id integer ,
	PRIMARY KEY(afm,song_id),
	FOREIGN KEY (afm) REFERENCES SYNTHETHS(afm) ON DELETE CASCADE,
	FOREIGN KEY (song_id) REFERENCES  TRAGOUDI(song_id) ON DELETE CASCADE
);

CREATE TABLE ERMHNEYEI (
	artist_id integer ,
	song_id integer,
	PRIMARY KEY(artist_id,song_id),
	FOREIGN KEY (artist_id) REFERENCES PERSONA_GROUP(artist_id) ON DELETE CASCADE,
	FOREIGN KEY (song_id) REFERENCES  TRAGOUDI(song_id) ON DELETE CASCADE
);

CREATE TABLE ORGANOPAIKTHS (
	afm varchar(9) PRIMARY KEY,
	fullname varchar(50) NOT NULL,
	AMKA varchar(11) UNIQUE,
	city varchar(30),
	specialty varchar(10) NOT NULL
);

CREATE TABLE EPIK_ORGANOP (
	organo_afm varchar(9) PRIMARY KEY ,
	kin1 varchar(10) NOT NULL,
	kin2 varchar(10),
	stathero varchar(10),
	email varchar(40),
	FOREIGN KEY (organo_afm) REFERENCES ORGANOPAIKTHS(afm) ON DELETE CASCADE
);

CREATE TABLE SYMMETEXEI (
	afm varchar(9) ,
	song_id integer ,
	PRIMARY KEY(afm,song_id),
	FOREIGN KEY (afm) REFERENCES ORGANOPAIKTHS(afm) ON DELETE CASCADE,
	FOREIGN KEY (song_id) REFERENCES TRAGOUDI(song_id) ON DELETE CASCADE
);


CREATE TABLE EPIK_GRAF (
	graf_afm varchar(9) PRIMARY KEY ,
	kin1 varchar(10),
	kin2 varchar(10),
	stathero varchar(10),
	email varchar(40)NOT NULL,
	socialmedia varchar(40),
	FOREIGN KEY (graf_afm) REFERENCES GRAFISTAS(afm) ON DELETE CASCADE
);

CREATE TABLE YPAGETAI (
	album_id integer ,
	genre varchar(30) ,
	PRIMARY KEY(album_id,genre),
	FOREIGN KEY (album_id) REFERENCES ALBUM(album_id) ON DELETE CASCADE,
	FOREIGN KEY (genre) REFERENCES EIDOS(genre) ON DELETE CASCADE
);



CREATE TABLE PELATHS (
	afm varchar(9) PRIMARY KEY,
	name varchar(50) NOT NULL
);

CREATE TABLE PWLEITAI (
	album_id integer ,
	afm varchar(9) ,
	order_date datetime DEFAULT NOW(),
	eidos varchar(8) NOT NULL CHECK(eidos IN ('CD','DIGITAL','VINYL')),
	posothta integer DEFAULT 1 NOT NULL,
	item_profit integer NOT NULL,
	PRIMARY KEY(album_id,afm,order_date),
	FOREIGN KEY (album_id) REFERENCES ALBUM(album_id) ON DELETE CASCADE,
	FOREIGN KEY (afm) REFERENCES PELATHS(afm) ON DELETE CASCADE
);

CREATE TABLE EPIK_PELATH (
	client_afm varchar(9)PRIMARY KEY ,
	kin1 varchar(10),
	kin2 varchar(10),
	stathero varchar(10),
	email varchar(40) NOT NULL,
	FOREIGN KEY (client_afm)  REFERENCES PELATHS(afm) ON DELETE CASCADE
);

CREATE TABLE FYS_DIANOMH (
	afm varchar(9) PRIMARY KEY ,
	TK varchar(5) NOT NULL,
	address varchar(30) NOT NULL,
	city varchar(30) NOT NULL,
	FOREIGN KEY(afm) REFERENCES PELATHS(afm) ON DELETE CASCADE
);

CREATE TABLE PSIF_DIANOMH (
	afm varchar(9) PRIMARY KEY,
	adm_fee integer DEFAULT 0,
	FOREIGN KEY (afm) REFERENCES PELATHS(afm) ON DELETE CASCADE
);

CREATE TABLE EPIK_STUDIO (
	studio_afm varchar(9) PRIMARY KEY ,
	kin1 varchar(10),
	kin2 varchar(10),
	stathero varchar(10) NOT NULL,
	email varchar(40),
	socialmedia varchar(40),
	FOREIGN KEY (studio_afm) REFERENCES STUDIO(afm) ON DELETE CASCADE
);

